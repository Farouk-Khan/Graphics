#include "includes/libft.h"

char	*ft_strncpy(char *s1, char *s2, unsigned int len)
{
	int i;

	i = 0;
	while (i < len)
	{
		if (s2[i] != '\0')
		{
			s1[i] = s2[i];
			i++;
		}
		else
		{
			while (i < len)
			{
				s1[i] = '\0';
				i++;
			}
		}
	}
	return (s1);
}
